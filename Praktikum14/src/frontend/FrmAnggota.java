    public void tampilkanData(){
        Object[] namaKolom = new Object[] {"ID", "Nama", "Alamat", "Telepon"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Anggota> listAnggota = new Anggota().getAll();
        Object[] rowData = new Object[4];
        
        for (Anggota agt : listAnggota){
            rowData[0] = agt.getIdanggota();
            rowData[1] = agt.getNama();
            rowData[2] = agt.getAlamat();
            rowData[3] = agt.getTelepon();
            
            model.addRow(rowData);
        }
        
        tblAnggota.setModel(model);
    }
    
    public void cari(String keyword){
        Object[] namaKolom = new Object[] {"ID", "Nama", "Alamat", "Telepon"};
        
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(namaKolom);
        
        ArrayList<Anggota> listAnggota = new Anggota().search(keyword);
        Object[] rowData = new Object[4];
        
        for (Anggota agt : listAnggota){
            rowData[0] = agt.getIdanggota();
            rowData[1] = agt.getNama();
            rowData[2] = agt.getAlamat();
            rowData[3] = agt.getTelepon();
            
            model.addRow(rowData);
        }
        
        tblAnggota.setModel(model);
    }
