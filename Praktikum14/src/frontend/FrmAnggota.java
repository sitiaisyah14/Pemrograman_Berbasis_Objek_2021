    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String nama = txtNama.getText();
        String alamat = txtAlamat.getText();
        String telepon = txtTelepon.getText();
        
        if(!(nama.isEmpty() && alamat.isEmpty() && telepon.isEmpty())){
            Anggota agt = new Anggota();
            agt.setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
            agt.setNama(nama);
            agt.setAlamat(alamat);
            agt.setTelepon(telepon);
            agt.save();
            
            tampilkanData();
        }
        else{
            JOptionPane.showMessageDialog(null, "Silahkan isi nama, alamat, dan nomor telepon");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

